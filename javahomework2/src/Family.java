public class Family {
    String father;
    String mother;
    String child;

    public Family(String father, String mother, String child) {
        this.father = father;
        this.mother = mother;
        this.child = child;
    }

    public String getFather() {
        return father;
    }

    public String getMother() {
        return mother;
    }

    public String getChild() {
        return child;
    }
}



